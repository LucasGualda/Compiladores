    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String rutaActual = System.getProperty("user.dir");
    System.out.println("Ruta actual de trabajo: " + rutaActual);

    // Establece la ruta completa al ejecutable "dot" en Windows
    String rutaDot = "C:\\Program Files\\Graphviz\\bin\\dot.exe"; // Reemplaza con la ruta correcta en tu sistema

    // Establece la ruta completa al archivo DOT y al archivo de imagen resultante
    String rutaArchivoDot = rutaActual + "/archivo.dot";
    String rutaImagen = rutaActual + "/archivo.png"; // Puedes cambiar la extensión según el formato de imagen deseado

    try {
        // Configura el proceso para ejecutar "dot" con los argumentos adecuados
        ProcessBuilder pb = new ProcessBuilder(rutaDot, "-Tpng", "-o", rutaImagen, rutaArchivoDot);
        pb.redirectErrorStream(true);
        
        // Ejecuta el proceso
        Process process = pb.start();

        // Espera a que el proceso termine
        int exitCode = process.waitFor();
        if (exitCode == 0) {
            System.out.println("Imagen generada exitosamente: " + rutaImagen);
        } else {
            System.err.println("Error al generar la imagen.");
        }
    } catch (IOException | InterruptedException e) {
        e.printStackTrace();
        System.err.println("Error al ejecutar dot: " + e.getMessage());
    }
        
    }//GEN-LAST:event_jButton2ActionPerformed

